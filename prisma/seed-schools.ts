async function seedSchools() {
  console.log('ðŸŒ± Starting to seed Canadian schools...')

  for (const school of canadianSchools) {
    const existing = await prisma.canadianSchool.findFirst({
      where: { name: school.name }
    })

    if (existing) {
      await prisma.canadianSchool.update({
        where: { id: existing.id },
        data: school,
      })
      console.log(`ðŸ”„ Updated: ${school.name}`)
    } else {
      await prisma.canadianSchool.create({
        data: school,
      })
      console.log(`âœ… Created: ${school.name}`)
    }
  }

  console.log('ðŸŽ‰ Successfully seeded all Canadian schools!')

  const count = await prisma.canadianSchool.count()
  console.log(`ðŸ“Š Total schools in database: ${count}`)
}

seedSchools()
  .catch((error) => {
    console.error('âŒ Error seeding schools:', error)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
